package Models;

public class Arquibancada {
	
	private int id;
	
	private String nome;
	private String inicioSantos;
	private String fimSantos;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getInicioSantos() {
		return inicioSantos;
	}
	public void setInicioSantos(String inicioSantos) {
		this.inicioSantos = inicioSantos;
	}
	public String getFimSantos() {
		return fimSantos;
	}
	public void setFimSantos(String fimSantos) {
		this.fimSantos = fimSantos;
	}
	

}
